int pierw(unsigned int x)
{
    int pocz = 0;
    int kon = x;
    int sr;

    while (kon - pocz > 1)
    {
        sr = (pocz + kon) / 2;
        if (sr * sr <= x)
            pocz = sr;
        else
            kon = sr;
    }
    if (x <= 1)
        return kon;
    else
        return pocz;
}

